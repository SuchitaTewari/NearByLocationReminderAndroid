public class ReminderList {

    String add_title;
    String reminder_note;
    String place_name;
    String address;

    public String getAdd_title() {
        return add_title;
    }

    public void setAdd_title(String add_title) {
        this.add_title = add_title;
    }

    public String getReminder_note() {
        return reminder_note;
    }

    public void setReminder_note(String reminder_note) {
        this.reminder_note = reminder_note;
    }

    public String getPlace_name() {
        return place_name;
    }

    public void setPlace_name(String place_name) {
        this.place_name = place_name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }


}
